import java.util.*;
public class typeCasting {
   public static void main(String[] args) {
   int a = 10;
   int b = 10;
   int c = 5;
   System.out.println((a==b));
   System.out.println((a!=b));
   System.out.println((a>=b));
   System.out.println((a<=b));
   System.out.println((c<b));
   System.out.println((c>b));

   } 
}
